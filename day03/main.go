package main

import (
	"fmt"
	"strings"
)

func main() {
	partOne()
	partTwo()
}

func partOne() {
	lines := strings.Split(input, "\n")

	height := len(lines)
	width := len(lines[0])

	x := 0
	y := 0

	xStep := 3
	yStep := 1

	count := 0
	for y < height-yStep {
		x += xStep
		y += yStep
		if lines[y][x%width] == '#' {
			count++
		}
	}

	fmt.Println(count)
}

func partTwo() {
	lines := strings.Split(input, "\n")

	pairs := []Pair{
		{
			x: 1,
			y: 1,
		},
		{
			x: 3,
			y: 1,
		},
		{
			x: 5,
			y: 1,
		},
		{
			x: 7,
			y: 1,
		},
		{
			x: 1,
			y: 2,
		},
	}

	height := len(lines)
	width := len(lines[0])

	result := 1
	for _, p := range pairs {
		x := 0
		y := 0

		count := 0
		for y < height-p.y {
			x += p.x
			y += p.y
			if lines[y][x%width] == '#' {
				count++
			}
		}
		result *= count
	}
	fmt.Println(result)
}

